def provision_client(client_id: str, plan: str) -> str:
    """Register a new client in Supabase using the shared Railway service.

    Args:
        client_id: Unique client identifier.
        plan: Subscription tier — basic | pro | enterprise.

    Returns:
        The shared Railway URL used for this client.

    Raises:
        KeyError: If a required env var is missing.
    """
    railway_url = os.environ["RAILWAY_URL"]

    print(f"[1/1] Registering {client_id!r} ({plan} plan) in Supabase...")
    from core.db import _get_client as _supabase_client

    api_key = "sk-" + secrets.token_urlsafe(32)
    _supabase_client().table("clients").upsert(
        {
            "client_id": client_id,
            "api_keys": {api_key: {"label": "default", "created_at": datetime.now(timezone.utc).isoformat()}},
            "enabled_skills": _PLAN_SKILLS[plan],
            "plan_level": plan,
            "railway_url": railway_url,
        },
        on_conflict="client_id",
    ).execute()

    gateway_url = f"https://skills-portal.skills-library.workers.dev/{client_id}/mcp"
    print(f"""
Done. {client_id!r} ({plan} plan) is live.

  Railway URL : {railway_url}
  Gateway URL : {gateway_url}
  API key     : {api_key}
  Skills      : {", ".join(_PLAN_SKILLS[plan])}

Claude Desktop config (~/.claude.json mcpServers block):
  "{client_id}-skills": {{
    "type": "http",
    "url": "{gateway_url}",
    "headers": {{"Authorization": "Bearer {api_key}"}}
  }}
""")
    return railway_url


def main() -> None:
    parser = argparse.ArgumentParser(description="Provision a new client MCP instance.")
    parser.add_argument("--client", required=True, help="Unique client identifier")
    parser.add_argument(
        "--plan",
        choices=["basic", "pro", "enterprise"],
        default="basic",
        help="Subscription tier",
    )
    args = parser.parse_args()

    try:
        provision_client(args.client, args.plan)
    except KeyError as e:
        print(f"[provision] Missing env var: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
